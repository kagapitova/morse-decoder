function decode(expr) {
    let arr1 = spliceIntoChunks(expr.split(''), 10);
    console.log(arr1)
    for (let i = 0; i < arr1.length; i++) {
        arr1[i] = arr1[i].join('').replaceAll('00', '').replaceAll('10', '.').replaceAll('11', '-').replaceAll('**********', ' ')
        if (arr1[i] !== ' ') {
            arr1[i] = MORSE_TABLE[arr1[i]]
        }
    }
    return arr1.join('')
}

function spliceIntoChunks(arr, chunkSize) {
    const res = [];
    while (arr.length > 0) {
        const chunk = arr.splice(0, chunkSize);
        res.push(chunk);
    }
    return res;
}

